
public class Cat {
  private int id;
  private String name;
  private Boolean status;
  private String location;
  private Timestamp date;
  private String description;

  public Cat() {
    this.id = id;
    this.name = name;
    this.status = status;
    this.location = location;
    this.date = date;
    this.description = description;
  }

  public getId() {
    return id;
  }

  public getName() {
    return name;
  }

  public getStatus() {
    return status;
  }

  public getLocation() {
    return location;
  }

  public getDate() {
    return date;
  }

  public getDescription() {
    return description;
  }

}
